	public List<GeographicPoint> bfs(GeographicPoint start, 
			 					     GeographicPoint goal, Consumer<GeographicPoint> nodeSearched)
	{
		// TODO: Implement this method in WEEK 2
		//Initialization part
		Queue<GeographicPoint> queue = new LinkedList<>();
		Set<GeographicPoint> visitedNodes = new HashSet<>();
		Set<GeographicPoint> toBeVisitedNodes = new HashSet<>();
		queue.add(start);
		while(!queue.isEmpty())
		{
			GeographicPoint top = queue.remove();
			visitedNodes.add(top);
			//System.out.println(top);
			for(mapEdges g : Nodes.get(top).edges)
			{
				if(!visitedNodes.contains(g.getEnd()))
				{
					toBeVisitedNodes.add(g.getEnd());
				}
				if(g.getEnd() == goal)
				{
					visitedNodes.add(goal);
					break;
				}
			}
			System.out.println("loop over");
			for(GeographicPoint g : toBeVisitedNodes)
			{
				System.out.println(g);
			}
			queue.addAll(toBeVisitedNodes);
			toBeVisitedNodes.removeAll(toBeVisitedNodes);
		}
		
		// Hook for visualization.  See writeup.
		//nodeSearched.accept(next.getLocation());
		List<GeographicPoint> list = new LinkedList<>();
		list.addAll(visitedNodes);
		System.out.println("BFS completed");
		for(GeographicPoint o : list)
		{
			System.out.println(o + "->");
		}
		return list;
	}
	

